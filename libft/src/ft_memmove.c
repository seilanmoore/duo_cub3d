#include "../include/libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char		*ptr_dst;
	const char	*ptr_src;

	if (dst > src)
	{
		ptr_dst = dst + len;
		ptr_src = src + len;
		while (len-- > 0)
			*(--ptr_dst) = *(--ptr_src);
	}
	else
		ft_memcpy(dst, src, len);
	return (dst);
}
